var idx = 0;
var correct = 0;
var wrong = 0;
var wrongList = [];
var shuffled = [];

function shuffle(arr){
  var a = arr.slice();

  for(var i = a.length - 1; i > 0; i--){
    var j = Math.floor(Math.random() * (i + 1));

    var tmp = a[i];
    a[i] = a[j];
    a[j] = tmp;
  }

  return a;
}

document.getElementById('startBtn').onclick = function(){
  shuffled = shuffle(Q);

  idx = 0;
  correct = 0;
  wrong = 0;
  wrongList = [];

  document.getElementById('start').style.display = 'none';
  document.getElementById('result').style.display = 'none';
  document.getElementById('quiz').style.display = 'block';

  showQ();
};

document.getElementById('rbtn').onclick = function(){
  document.getElementById('result').style.display = 'none';
  document.getElementById('start').style.display = 'block';
};

document.getElementById('nxt').onclick = function(){
  idx++;

  if(idx >= shuffled.length){
    showResult();
  } else {
    showQ();
  }
};

function showQ(){
  var q = shuffled[idx];

  var pct = Math.round((idx / shuffled.length) * 100);

  document.getElementById('pb').style.width = pct + '%';

  document.getElementById('pt').textContent =
    (idx + 1) + ' / ' + shuffled.length;

  document.getElementById('badge').textContent = q.s;

  document.getElementById('qtxt').textContent = q.q;

  document.getElementById('expl').className = 'expl';
  document.getElementById('expl').textContent = '';

  document.getElementById('nxt').style.display = 'none';

  var opts = document.getElementById('opts');

  opts.innerHTML = '';

  for(var i = 0; i < q.a.length; i++){

    (function(i){

      var btn = document.createElement('button');

      btn.className = 'opt';

      btn.textContent =
        ['A) ','B) ','C) '][i] + q.a[i];

      btn.onclick = function(){
        answer(i);
      };

      opts.appendChild(btn);

    })(i);
  }
}

function answer(chosen){

  var q = shuffled[idx];

  var btns =
    document.getElementById('opts')
    .querySelectorAll('button');

  for(var i = 0; i < btns.length; i++){
    btns[i].disabled = true;
  }

  btns[q.c].className = 'opt reveal';

  if(chosen === q.c){

    btns[chosen].className = 'opt correct';

    correct++;

  } else {

    btns[chosen].className = 'opt wrong';

    wrong++;

    wrongList.push({
      q: q.q,
      ca: q.a[q.c],
      e: q.e
    });

    var expl = document.getElementById('expl');

    expl.textContent = q.e;

    expl.className = 'expl show';
  }

  document.getElementById('nxt').style.display = 'block';
}
